// for loop
for(i=1;i<5;i++){
    console.log(i)
}

for(i=10;i>=1;i--){
    console.log(i)
}

// while loop
i=1;
while(i<10){
    console.log(i)
    i++
}

i=20;
while(i>10){
    console.log(i)
    i--
}

// do while loop
i=10
do{
    console.log(i)
    i++
}
while(i<=15);

i=35
do{
    console.log(i)
    i--
}
while(i>30);